def lookup(query):
    """查询产品库存"""
    # 精确匹配
    if query in stock_data:
        return [(query, stock_data[query][0], stock_data[query][1])]

    # 部分匹配
    results = []
    for name, (status, price) in stock_data.items():
        if query in name or name in query:
            results.append((name, status, price))

    return results


